#include <fstream>
#include <iostream>
using namespace std;

int main()
{
   ofstream fout("test1.txt");
   if(!fout){
      cout << "ファイルをオープンできませんでした。\n";
      return 1;
   }
   else
      cout  << "ファイルをオープンしました。\n";

   fout << "Hello!\n";
   fout << "Goodbye!\n";
   cout << "ファイルに書き込みました。\n";

   fout.close();

   return 0;
}
